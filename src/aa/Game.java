    public boolean gameHasEnded(){
        for (Card i: gp.cardsMatrix){
            if (i.status != 2){
                return false;
            }
        }
        return true;
    }
    
    
    public void checkGameStatus(){
        if (!gameHasEnded()){
            mp.setVisible(false);
            gp.setVisible(false);
            setEndPanel();
            ep.setVisible(true);
        }
    }
    
